# prompt adapted from InspireScore: https://github.com/fywang12/InspireDebate/blob/main/inspirescore.py
import argparse
import uvicorn
from dotenv import load_dotenv
load_dotenv()

from google.adk.agents import Agent
from google.adk.tools import FunctionTool
from google.adk.a2a.utils.agent_to_a2a import to_a2a

from agentbeats.tool_provider import ToolProvider
from models import DebateEval, judge_agent_card


system_prompt = '''
You are the green agent judge for code generated by multiple PurpleAgents.

Participating agents:
- **PurpleAgent_i**: arbitrary code generation agents (i=1,2,...,N)

Your role is to judge the winner based on the recall of the generated code against the reference solution.

You will receive a structured input containing:
- the URLs used by PurpleAgents_i - use them to communicate with agents using the talk_to_agent tool
- number of rounds (num_rounds) - the number of code generation rounds to conduct

Once you receive this, immediately start following these instructions.

### Instructions:

1. Orchestrate the evaluation using the talk_to_agent tool:
- For each round:
  - Provide all PurpleAgents_i with the same query from the benchmark.
  - Each PurpleAgent_i generates code in response to the query.
  - Continue this exchange until the specified number of rounds (num_rounds) is reached.

2. After all rounds have finished:
  - Use SOCBenchSC analysis to compute the recall for each PurpleAgent_i.
  - Compute the final average recall score per agent.
  - Determine the winner as the PurpleAgent_i with the highest average recall score.

### Output Format:

1. **PurpleAgent_i Scores**:
    - PurpleAgent_1: [score]
    - PurpleAgent_2: [score]
    - ...
    - PurpleAgent_N: [score]

2. **Winner**: [PurpleAgent_i]
'''


def main():
    parser = argparse.ArgumentParser(description="Run the A2A evaluation judge.")
    parser.add_argument("--host", type=str, default="127.0.0.1", help="Host to bind the server")
    parser.add_argument("--port", type=int, default=9009, help="Port to bind the server")
    parser.add_argument("--card-url", type=str, help="External URL to provide in the agent card")
    args = parser.parse_args()

    tool_provider = ToolProvider()
    root_agent = Agent(
        name="code_judge",
        model="moonshotai/Kimi-K2-Instruct",
        description=(
            "Judge generated code from multiple agents on a given query with multiple rounds of code generation."
        ),
        instruction=system_prompt,
        tools=[FunctionTool(func=tool_provider.talk_to_agent)],
        output_schema=DebateEval,
        after_agent_callback=lambda callback_context: tool_provider.reset()
    )

    agent_card = judge_agent_card("DebateJudgeADK", args.card_url or f"http://{args.host}:{args.port}/")
    a2a_app = to_a2a(root_agent, agent_card=agent_card)
    uvicorn.run(a2a_app, host=args.host, port=args.port)


if __name__ == "__main__":
    main()
